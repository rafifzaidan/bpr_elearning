"use client";

import Link from "next/link";
import Image from "next/image";
import { usePathname } from "next/navigation";
import { useState } from "react";

const navItems = [
  { href: "/", label: "Dashboard", icon: DashboardIcon },
  { href: "/users", label: "Pegawai", icon: UsersIcon },
  { href: "/modules", label: "Modul", icon: BookIcon },
  { href: "/questions", label: "Bank Soal", icon: QuestionIcon },
  { href: "/exams", label: "Ujian", icon: ClipboardIcon },
  { href: "/results", label: "Hasil", icon: ChartIcon },
];

export default function Sidebar() {
  const pathname = usePathname();
  const [collapsed, setCollapsed] = useState(false);

  return (
    <>
      {/* Mobile overlay */}
      <aside
        className={`
          fixed top-0 left-0 z-40 h-screen flex flex-col
          bg-[var(--color-surface-sidebar)] text-white
          transition-all duration-300 ease-in-out
          ${collapsed ? "w-[68px]" : "w-[260px]"}
        `}
      >
        {/* Logo / Brand */}
        <div className="flex items-center gap-3 px-5 h-16 border-b border-white/10 shrink-0">
          <div className="w-8 h-8 flex items-center justify-center shrink-0">
            <Image 
              src="/logo.png" 
              alt="BPR Jatim Logo" 
              width={32} 
              height={32} 
              className="object-contain"
            />
          </div>
          {!collapsed && (
            <div className="overflow-hidden whitespace-nowrap">
              <p className="font-bold text-sm leading-tight text-white">Bank BPR</p>
              <p className="text-[11px] text-slate-400">Jawa Timur</p>
            </div>
          )}
        </div>

        {/* Navigation */}
        <nav className="flex-1 py-4 px-3 space-y-1 overflow-y-auto">
          {navItems.map((item) => {
            const isActive =
              pathname === item.href ||
              (item.href !== "/" && pathname.startsWith(item.href));
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`
                  flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium
                  transition-all duration-200
                  ${
                    isActive
                      ? "bg-blue-600 text-white shadow-lg shadow-blue-600/25"
                      : "text-slate-300 hover:bg-[var(--color-surface-sidebar-hover)] hover:text-white"
                  }
                  ${collapsed ? "justify-center" : ""}
                `}
                title={collapsed ? item.label : undefined}
              >
                <item.icon className="w-5 h-5 shrink-0" />
                {!collapsed && <span>{item.label}</span>}
              </Link>
            );
          })}
        </nav>

        {/* Collapse toggle */}
        <div className="px-3 py-3 border-t border-white/10 shrink-0">
          <button
            onClick={() => setCollapsed(!collapsed)}
            className="w-full flex items-center justify-center gap-2 px-3 py-2 rounded-lg text-sm text-slate-400 hover:bg-[var(--color-surface-sidebar-hover)] hover:text-white"
          >
            {collapsed ? (
              <ChevronRightIcon className="w-5 h-5" />
            ) : (
              <>
                <ChevronLeftIcon className="w-5 h-5" />
                <span>Collapse</span>
              </>
            )}
          </button>
        </div>
      </aside>

      {/* Spacer to push content */}
      <div
        className={`shrink-0 transition-all duration-300 ${
          collapsed ? "w-[68px]" : "w-[260px]"
        }`}
      />
    </>
  );
}

